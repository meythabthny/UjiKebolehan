    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double Tinggi = Double.valueOf(tinggi.getText());
        double Berat = Double.valueOf(berat.getText());
        double ideal=0;
        String temp;
        String nm = nama.getText();
        String ti = tinggi.getText();
        String br = berat.getText();

        if(nama.equals("")||tinggi.equals("")||br.isEmpty()){
            try{
                JOptionPane.showMessageDialog(null, "Masukkan data dengan benar !","Kesalahan", JOptionPane.INFORMATION_MESSAGE);
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "error"+e,"Kesalahan", JOptionPane.WARNING_MESSAGE);

            }

        }

        if(perempuan.isSelected()){
            ideal = (Tinggi - 104) *1;
        }
        else if (laki.isSelected()){
            ideal = (Tinggi - 100) * 1;
        }
        temp = Double.toString(ideal);

        if (ideal < Berat){
            Ideal.setText(temp);
            hasil.setText("Maaf "+nama.getText()+", Sepertinya anda Overweight :(");
            saran.setText("Banyaklah berolahraga dan hindari makanan berkolestrol");
        }else if (ideal > Berat){
            Ideal.setText(temp);
            hasil.setText("Maaf "+nama.getText()+", Sepertinya anda Underweight :(");
            saran.setText("Banyaklah mengonsumsi makanan yang berkarbohidrat");
        }else {
            Ideal.setText(temp);
            hasil.setText("Maaf "+nama.getText()+", Berat badan anda sudah ideal :)");
            saran.setText("Lanjutkan pola makan teratur dan gaya hidup sehat");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

